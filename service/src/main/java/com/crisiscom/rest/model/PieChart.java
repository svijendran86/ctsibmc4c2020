package com.crisiscom.rest.model;

public class PieChart {

	 private String sleeptype;

     private int sleeptime;

     private String color;

     private String legendFontColor;

     private int legendFontSize;



     /**

     * @return the sleeptype

     */

     public String getSleeptype() {

                     return sleeptype;

     }



     /**

     * @param sleeptype

     *            the sleeptype to set

     */

     public void setSleeptype(String sleeptype) {

                     this.sleeptype = sleeptype;

     }



     /**

     * @return the sleeptime

     */

     public int getSleeptime() {

                     return sleeptime;

     }



     /**

     * @param sleeptime

     *            the sleeptime to set

     */

     public void setSleeptime(int sleeptime) {

                     this.sleeptime = sleeptime;

     }



     /**

     * @return the color

     */

     public String getColor() {

                     return color;

     }



     /**

     * @param color

     *            the color to set

     */

     public void setColor(String color) {

                     this.color = color;

     }



     /**

     * @return the legendFontSize

     */

     public int getLegendFontSize() {

                     return legendFontSize;

     }



     /**

     * @param legendFontSize

     *            the legendFontSize to set

     */

     public void setLegendFontSize(int legendFontSize) {

                     this.legendFontSize = legendFontSize;

     }



     /**

     * @return the legendFontColor

     */

     public String getLegendFontColor() {

                     return legendFontColor;

     }



     /**

     * @param legendFontColor

     *            the legendFontColor to set

     */

     public void setLegendFontColor(String legendFontColor) {

                     this.legendFontColor = legendFontColor;

     }
	
}
